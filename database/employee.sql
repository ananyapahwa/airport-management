CREATE TABLE Employee (
    E_ID VARCHAR2(10) PRIMARY KEY,
    F_NAME VARCHAR2(50),
    M_NAME VARCHAR2(50),
    L_NAME VARCHAR2(50),
    SEX CHAR(1),
    SSN VARCHAR2(20),
    ADDRESS VARCHAR2(150),
    CITY VARCHAR2(50),
    STATE VARCHAR2(50),
    COUNTRY VARCHAR2(50),
    PHONE VARCHAR2(15),
    SALARY FLOAT,
    JOB VARCHAR2(50) 
);
